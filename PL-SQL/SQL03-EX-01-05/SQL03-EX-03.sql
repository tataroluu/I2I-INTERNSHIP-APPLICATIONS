CREATE TABLE 
    EMPLOYEES
AS 
(
    SELECT 
        * 
    FROM 
        HR.EMPLOYEES
);
  
ALTER TABLE 
    EMPLOYEES
ADD 
    DEPARTMENT_NAME VARCHAR(30);
    
MERGE INTO 
    EMPLOYEES e
USING 
(
    SELECT 
        DEPARTMENT_ID, 
        DEPARTMENT_NAME 
    FROM 
        HR.DEPARTMENTS) d
ON 
(
    e.DEPARTMENT_ID = d.DEPARTMENT_ID
)
WHEN MATCHED THEN
UPDATE SET 
    e.DEPARTMENT_NAME = d.DEPARTMENT_NAME;
